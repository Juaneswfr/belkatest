            <h2>¿Cómo se inventaron las fajas?</h2>
            <p>Para hablar de las fajas modernas, debemos devolvernos hasta la antigüedad, donde las
            mujeres de Grecia, Roma, Creta, Egipto y Siria utilizaban tiras y cinturones alrededor de su
            cuerpo, apareciendo así los primeros corsé; aunque en aquel momento no se les conocía con
            ese nombre, cumplían la misma función: “destacar su figura levantando sus senos”, como
            menciona Silvia Abreu Reyes, patronista y modista, en su publicación “La historia del corsé”.</p>
            <blockquote><p> Te puede interesar: <a href="https://www.fajasfajitex.com/blog/articulo?titulo=Conoce-las-fajas-de-alta-compresi%C3%B3n" target="_blank">Conoce las fajas de alta compresión</a> </p></blockquote>
            <p>Luego, el también conocido como ceñidor, empezó a tener presencia a través de la historia,
            incluso Homero en el siglo IX menciona unas “envolturas alrededor del cuerpo” que eran
            usadas por las mujeres. Para el siglo XVI aparece entonces el precursor del corsé en España,
            lo nombraron “cartón de pecho” y se fue popularizando en Europa hasta que en 1550 Catalina
            Médicis, esposa del Rey Henri II de Francia, hizo mandatorio el uso del ajustador en las
            mujeres de la corte y como bien sabemos, la realeza y las familias con poder eran las revistas
            de tendencias del pasado. Este auge llevó a que el uso del corsé pasara a ser poco
            saludable, además, no era tan accesible como las fajas de la actualidad.</p>

            <p>Ya para la Revolución Industrial se democratizó el acceso a las mismas, permitiendo que
            muchas más personas pudieran usarlo. Con el tiempo se des popularizaron un poco, hasta
            que nuevamente Christian Dior las trajo a la mesa en 1947, confirmando, una vez más, que la
            moda es cíclica.</p>
            
